    //acao de calcular as vendas e comissoes do mes e preencher a tabela
    private void buttonCalculaMesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCalculaMesActionPerformed
        //checa se existe algum dado inserido
        if (textMes.getText().equals("")||textAno.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Atenção! Digite um valor.", "Aviso", JOptionPane.WARNING_MESSAGE);
        } 
        else{
            double total, comissao;
            
            Set<Vendedor> setVend = vendas.getVendasImportadas().keySet();

            //criacao da tabela atraves do DefaultTableModel
            DefaultTableModel model = new DefaultTableModel();
            JTable tabela = new JTable(model);
            model.addColumn("Nome");
            model.addColumn("Total de vendas");
            model.addColumn("Total de comissão");

            DecimalFormat df = new DecimalFormat("0.00");
           
            Iterator<Vendedor> percorre = setVend.iterator();
            
            /*percorre o conjunto de vendedores calculando a venda e a comissao do periodo indicado.
             * Apos isso armazena os resultados em String[] e adiciona na linha da tabela
             */
            while(percorre.hasNext()){
                Vendedor vende = percorre.next();
                total=vendas.calculaVendaMes(vende, Integer.parseInt(textMes.getText()), Integer.parseInt(textAno.getText()) );
                comissao = calcula.comissaoVendedorMes(vendas, vende, Integer.parseInt(textMes.getText()), Integer.parseInt(textAno.getText()));          
                String[] campos={vende.getNome(), df.format(total), df.format(comissao)};          

                model.addRow(campos);
            }    
            
            //Inicializa um JDialog para mostrar a tabela
            JFrame frame = new JFrame();
            JDialog dialog = new JDialog(frame, "Relatório", true);
            
            JScrollPane pane = new JScrollPane(tabela);
            dialog.getContentPane().setLayout(new FlowLayout());
            dialog.getContentPane().add(pane);  
            dialog.pack();
            dialog.setVisible(true);
              
        }               
    }//GEN-LAST:event_buttonCalculaMesActionPerformed

