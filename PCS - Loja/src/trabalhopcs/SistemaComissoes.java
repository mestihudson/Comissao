    //Acao de cadastrar produtos
    private void buttonProdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonProdActionPerformed
        int returnVal = fileChooser.showOpenDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
        File file = fileChooser.getSelectedFile();
        try {
            servico.gerenciaImportacaoProdutos(file, produtos);
        } catch (IOException ex) {
          System.out.println("problema no acesso ao arquivo"+file.getAbsolutePath());
        }
    } else {
        System.out.println("Acesso cancelado pelo usuário");
    }
    }//GEN-LAST:event_buttonProdActionPerformed

